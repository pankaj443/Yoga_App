package com.py.yoga.object;

public class tips {

   private int id;
  private int title;
  private int image;

    public tips(int id, int title, int image) {
        this.id = id;
        this.title = title;

        this.image = image;
    }

    public int getId() {
        return id;
    }

    public int getTitle() {
        return title;
    }



    public int getImage() {
        return image;
    }
}
